// Helper function to send edited messages
async function sendEditedMessages(conn, from, initialText, messages, delay = 1000) {
    try {
        const loadingMessage = await conn.sendMessage(from, { text: initialText });
        
        for (const line of messages) {
            await new Promise(resolve => setTimeout(resolve, delay));
            await conn.relayMessage(
                from,
                {
                    protocolMessage: {
                        key: loadingMessage.key,
                        type: 14,
                        editedMessage: {
                            conversation: line,
                        },
                    },
                },
                {}
            );
        }
    } catch (e) {
        throw e;
    }
}

